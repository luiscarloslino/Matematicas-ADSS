# Ademas considere que la variable pueden ser reasignada conservando la ultima informacion
# contenida indistinta del tipo de dato. Esto en otros lenguajes de programacion no es asi de facil
book_preferido = 'Padre pobre Padre rico'  # Ejemplo 1ra asignacion
print(book_preferido)

# 2da asignacion (reasignacion) cambiando su dato y tipo de dato
book_preferido = 300.025
print(book_preferido)

# La variable solo conserva Las 7 semillas en este momento del programa
book_preferido = 'Las 7 semillas'
print(book_preferido)
# Padre pobre Padre rico
# 300.025
# Las 7 semillas
