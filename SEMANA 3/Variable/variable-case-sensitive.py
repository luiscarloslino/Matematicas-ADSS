# Eso quiere decir que diferencia nombre de mayusculas de minusculas y no son lo mismo por ejemplo:
# dato1 o Dato1 o DATO1, los 3 son nombres diferentes en programacion eso es case sensitive
# Tambien es importante tomar en cuenta las CONVENCIONES para su uso asi tenemos las 3 mas utilizadas
