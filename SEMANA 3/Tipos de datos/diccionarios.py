# Dict - Los diccionarios agrupan datos que pertenecen a una misma entidad

{
    'pais': 'Peru',
    'capital': 'Lima',
    'continente': 'America del Sur',
}
print({
    'pais': 'Peru',
    'capital': 'Lima',
    'continente': 'America del Sur',
})
print(type({
    'pais': 'Peru',
    'capital': 'Lima',
    'continente': 'America del Sur',
}))
# {'pais': 'Peru', 'capital': 'Lima', 'continente': 'America del Sur'}
# <class 'dict'>
