# Aunque hay varios tipos de numeros los mas utilizados y al que dedicaremos esta informacion es
# numeros enteros (int) y numeros decimales en python llamados flotantes (float)

10  # entero
20.45  # flotante
print(type(10))
print(type(20.45))
print(0o123)  # octales
print(type(0o123))
print(0x123)  # hexadecimales
print(type(0x123))
print(6.62607E-34)  # 6.62607 x 10-34  # 6.62607e-34
print(6.62607e-34)  # 6.62607 x 10-34  # 6.62607e-34
print(type(6.62607E-34))
# <class 'int'>
