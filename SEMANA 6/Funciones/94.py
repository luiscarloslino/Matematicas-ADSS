# Podemos hacer operaciones
def suma1(numero1, numero2):
    return int(numero1)+int(numero2)


primer_numero = input('Ingrese primer numero:  ')
segundo_numero = input('Ingrese segundo numero: ')
print('La suma de los 2 numeros es: ', end='')
print(suma1(primer_numero, segundo_numero))

print('\n')
# La suma de los 2 numeros es: 134
